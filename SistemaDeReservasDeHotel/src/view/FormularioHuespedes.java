    public FormularioHuespedes(ListadoHuespedes parent, boolean modal, String Mode, long id) {
        super(parent, modal);    
        this.parent = parent;
        initComponents();
        
        this.Mode = Mode;
        this.id = id;
        
        huesped = new Huesped();        
        
        if(Mode.equals("UPD") || Mode.equals("DLT")){
            huesped = (Huesped) dao.getById(id);
            txt_field_Nombre.setText(huesped.getNombre());
            txt_field_Dni.setText(huesped.getDni());
        }
        
        if(Mode.equals("INS")){
            btn_operacion.setText("Insertar");
        }else if(Mode.equals("UPD")){
            btn_operacion.setText("Modificar");
        }else if(Mode.equals("DLT")){
            btn_operacion.setText("Borrar");
        }
        
    }
    
    private boolean validar(){
        if(txt_field_Nombre.getText().equals(null) || txt_field_Nombre.getText().equals("")){
            JOptionPane.showMessageDialog(parent, "Debe ingresar un nombre.", "ERROR DE VALIDACIÓN", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        if(txt_field_Dni.getText().equals(null) || txt_field_Dni.getText().equals("")){
            JOptionPane.showMessageDialog(parent, "Debe ingresar un DNI.", "ERROR DE VALIDACIÓN", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        return true;
    }

