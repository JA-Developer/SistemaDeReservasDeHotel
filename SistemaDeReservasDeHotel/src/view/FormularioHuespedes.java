    private void btn_operacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_operacionActionPerformed
        // TODO add your handling code here:
        if(Mode.equals("INS")){
            boolean validacion = validar();
            if(validacion){
                Huesped newHuesped = new Huesped();
                newHuesped.setNombre(txt_field_Nombre.getText());
                newHuesped.setDni(txt_field_Dni.getText());
                
                boolean operacion = dao.insert(newHuesped);
                if(operacion){
                    parent.refrescarDatos();
                    this.dispose();
                }
            }
        }
        if(Mode.equals("UPD")){
            boolean validacion = validar();
            if(validacion){
                huesped.setNombre(txt_field_Nombre.getText());
                huesped.setDni(txt_field_Dni.getText());
                
                boolean operacion = dao.update(huesped);
                if(operacion){
                    parent.refrescarDatos();
                    this.dispose();
                }
            }
        }
        if(Mode.equals("DLT")){
            boolean operacion = false;
            try {
                operacion = dao.delete(huesped.getId());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(parent, "Ha ocurrido un error: " + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            if(operacion){
                parent.refrescarDatos();
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(parent, "No fue posible eliminar el objeto seleccionado", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_operacionActionPerformed

