    private boolean validar(){
        if(txt_field_Rol.getSelectedItem().equals(null)){
            JOptionPane.showMessageDialog(parent, "Debe ingresar un rol.", "ERROR DE VALIDACIÓN", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        try {
            String rol = txt_field_Rol.getSelectedItem().toString();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(parent, "Debe ingresar un valor válido para el rol.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        if(txt_field_Usuario.getText().equals(null) || txt_field_Usuario.getText().equals("")){
            JOptionPane.showMessageDialog(parent, "Debe ingresar un nombre de usuario.", "ERROR DE VALIDACIÓN", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        if(Mode.equals("INS")){
            if(txt_field_Clave.getPassword().equals(null) || txt_field_Clave.getPassword().length <= 0){
                JOptionPane.showMessageDialog(parent, "Debe ingresar una clave.", "ERROR DE VALIDACIÓN", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        }
        
        return true;
    }

