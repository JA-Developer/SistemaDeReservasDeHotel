    private void btn_operacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_operacionActionPerformed

        String txt_field_Clave_Hash = "";
        try {
            if(txt_field_Clave.getPassword().length > 0)
            {
                // Obtener la instancia del algoritmo MD5
                MessageDigest md = MessageDigest.getInstance("MD5");

                // Obtener el hash en forma de bytes
                byte[] passwordHashBytes = md.digest((new String(txt_field_Clave.getPassword())).getBytes());

                // Convertir el hash a una representación hexadecimal
                StringBuilder passwordHexString = new StringBuilder();
                for (byte b : passwordHashBytes) {
                    // Convertir cada byte a un valor hexadecimal
                    passwordHexString.append(String.format("%02x", b));
                }

                txt_field_Clave_Hash = passwordHexString.toString();   
            }
        } catch (NoSuchAlgorithmException ex) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error: " + ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        
        if(Mode.equals("INS")){
            boolean validacion = validar();
            if(validacion){
                
                Usuario newUsuario = new Usuario();
                newUsuario.setUsername(txt_field_Usuario.getText());
                newUsuario.setPasswordHash(txt_field_Clave_Hash);
                newUsuario.setRol(txt_field_Rol.getSelectedItem().toString());
                
                boolean operacion = dao.insert(newUsuario);
                if(operacion){
                    parent.refrescarDatos();
                    this.dispose();
                }
            }
        }
        if(Mode.equals("UPD")){
            boolean validacion = validar();
            if(validacion){
                usuario.setUsername(txt_field_Usuario.getText());
                usuario.setPasswordHash(txt_field_Clave_Hash);
                usuario.setRol(txt_field_Rol.getSelectedItem().toString());
                
                boolean operacion = dao.update(usuario);
                if(operacion){
                    parent.refrescarDatos();
                    this.dispose();
                }
            }
        }
        if(Mode.equals("DLT")){
            boolean operacion = false;
            try {
                operacion = dao.delete(usuario.getId());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(parent, "Ha ocurrido un error: " + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            if(operacion){
                parent.refrescarDatos();
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(parent, "No fue posible eliminar el objeto seleccionado", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_operacionActionPerformed

